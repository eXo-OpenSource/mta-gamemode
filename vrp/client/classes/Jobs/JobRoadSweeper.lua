JobRoadSweeper = inherit(Job)

function JobRoadSweeper:constructor()
	Job.constructor(self, 16, 198.42, -1448.85, 13.02, 320, "Roadsweeper.png", "files/images/Jobs/HeaderRoadSweeper.png", _(HelpTextTitles.Jobs.RoadSweeper):gsub("Job: ", ""), _(HelpTexts.Jobs.RoadSweeper))
	self:setJobLevel(JOB_LEVEL_SWEEPER)

	self.m_Rubbish = {}

	-- add job to help menu
	HelpTextManager:getSingleton():addText("Jobs", _(HelpTextTitles.Jobs.RoadSweeper):gsub("Job: ", ""), "jobs.roadsweeper")
end

function JobRoadSweeper:start()
	local func = bind(self.Rubbish_Hit, self)

	for k, v in pairs(JobRoadSweeper.Rubbish) do
		local x, y, z = unpack(v)
		local object = createObject(2670, x, y, z, 0, 0, math.random(0, 360))
		local colShape = createColSphere(x, y, z, 5)
		setElementParent(object, colShape)
		addEventHandler("onClientColShapeHit", colShape, func)

		self.m_Rubbish[#self.m_Rubbish + 1] = colShape
	end

	-- Show text in help menu
	HelpBar:getSingleton():addText(_(HelpTextTitles.Jobs.RoadSweeper), _(HelpTexts.Jobs.RoadSweeper))
end

function JobRoadSweeper:stop()
	for k, v in pairs(self.m_Rubbish) do
		if v and isElement(v) then
			destroyElement(v)
		end
	end
	self.m_Rubbish = {}

	-- Reset text in help menu
	HelpBar:getSingleton():addText(_(HelpTextTitles.General.Main), _(HelpTexts.General.Main), false)
end

function JobRoadSweeper:Rubbish_Hit(hitElement, matchingDimension)
	if hitElement == localPlayer and matchingDimension and not self.m_Busy then
		local vehicle = getPedOccupiedVehicle(localPlayer)
		if not vehicle or getElementModel(vehicle) ~= 574 then -- Sweeper
			return
		end

		destroyElement(source)
		triggerServerEvent("sweeperGarbageCollect", root)
	end
end


